# https://docs.python.org/3/tutorial/controlflow.html
for i in [0, 1, 2, 3, 4]:
    print (i)

for i in range(5):
    print (i)

for i in range(2, 5):
    print (i)

for i in range(0, 21, 5):
    print (i)

nombres = ['Paulina', 'Moises', 'Ignacio','Hector','Raul','Jocelyn']

for nombre in nombres:
    print (nombre)

for caracter in nombre:
    print (caracter)